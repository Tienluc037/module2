<?php
$arr = [
    [2,5,9,7,6,3],
    [5,4,2,1,0,9],
    [4,5,6,7,8,9],
    [1,3,5,7,9,6],
    [3,5,6,2,3,0],
    [4,5,7,1,0,3]
];
$sum = 0;
for ($i = 0; $i < count($arr); $i++){
    for ($j = 0; $j < count($arr[$i]); $j++){
        if ($i === $j){
            $sum += $arr[$i][$j];
        }

    }
}
echo ($sum);
